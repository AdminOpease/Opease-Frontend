// ── Van driver shift schedule (generated) ─────────────────────────────
// Same seed-based algorithm as rotaDemoData.js generateSchedule()
// Keys: "driverId-weekNumber" → array of 7 shift codes (Sun–Sat)
function generateVanDriverSchedule() {
  const schedule = {};
  const codes = Object.keys(SHIFT_CODES);

  VAN_DRIVERS.forEach((driver) => {
    ROTA_WEEKS.forEach((week) => {
      const key = `${driver.id}-${week.weekNumber}`;
      const shifts = [];

      for (let d = 0; d < 7; d++) {
        const seed = driver.id * 7 + week.weekNumber * 3 + d;

        if (driver.left) {
          shifts.push(seed % 5 === 0 ? 'S' : 'R');
        } else {
          const dayType = seed % 10;
          if (d === 0 || d === 6) {
            // Weekends — more likely rest
            shifts.push(dayType < 6 ? 'R' : (dayType < 8 ? 'W' : 'SB'));
          } else if (dayType < 1) {
            shifts.push('H');
          } else if (dayType < 2) {
            shifts.push(['Office', 'MT', 'Fleet', 'SD'][seed % 4]);
          } else if (dayType < 3) {
            shifts.push('R');
          } else {
            shifts.push('W');
          }
        }
      }

      schedule[key] = shifts;
    });
  });

  return schedule;
}

